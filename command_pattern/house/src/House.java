public class House {
    private boolean lights;
    private boolean door;

    public boolean lightsAre() {
        return lights;
    }

    public boolean doorIs() {
        return door;
    }

    public void setLights(boolean lights) {
        this.lights = lights;
    }

    public void setDoor(boolean door) {
        this.door = door;
    }
}
